export const initI18n = (language: Locale) => {
  if (!i18n.isInitialized) {
    void i18n.use(initReactI18next).init({
      resources,
      lng: language,
      fallbackLng: "zh-CN",
      interpolation: { escapeValue: false },
    });
    return;
  }

  if (i18n.language !== language) {
    void i18n.changeLanguage(language);
  }
};

export default i18n;